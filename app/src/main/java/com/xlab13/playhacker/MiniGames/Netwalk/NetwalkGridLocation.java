package com.xlab13.playhacker.MiniGames.Netwalk;

public class NetwalkGridLocation {
    private final int column, row;

    public NetwalkGridLocation(int column, int row) {
        this.column = column;
        this.row = row;
    }

    public int getColumn() {
        return column;
    }

    public int getRow() {
        return row;
    }
}
